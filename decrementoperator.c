#include <stdio.h>
int main()
{
    //increment operator
    // --i (pre decrement)
    // i-- (post decrement)

    // for --i(pre decrement)
    int i=9;
    printf("%d\n", --i);
    printf("%d\n", i);

    // for n--(post decrement)
    int n=5;
    printf("%d\n", n--);
    printf("%d\n", n);


    return 0;
    
}