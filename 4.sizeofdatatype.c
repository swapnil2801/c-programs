//A program to find the size of various data types
#include <stdio.h>
int main()
{
    printf("The size of int data type is %d\n",sizeof(int));
    printf("The size of char data type is %d\n",sizeof(char));
    printf("The size of float data type is %d\n",sizeof(float));
    printf("The size of double data type is %d\n",sizeof(double));
    return 0;
}