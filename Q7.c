//Write a program to check if the given number is a natural number.
#include <stdio.h>
int main()
{
    int n;
    printf("Enter no. :");
    scanf("%d",&n);
    for (int i=0;i<=n;i++){
        if (i=n){
            printf("natural no");

        }else{
            printf("Not a natural no");}

    }return 0;

}